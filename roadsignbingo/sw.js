self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => cache.addAll(PRECACHE))
      .then(() => self.skipWaiting())
  );
});

self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(keys =>
      Promise.all(keys.filter(k => k !== CACHE_NAME).map(k => caches.delete(k)))
    ).then(() => self.clients.claim())
  );
});

self.addEventListener('fetch', event => {
  // Only handle same-origin and SVG/HTML/manifest requests
  const url = new URL(event.request.url);
  const isSameOrigin = url.origin === self.location.origin;
  const isLeaflet = url.href.includes('unpkg.com/leaflet');
  const isTile = url.href.includes('tile.openstreetmap.org');

  if (isTile) {
    // Don't cache map tiles — just fetch from network
    return;
  }

  if (isSameOrigin || isLeaflet) {
    event.respondWith(
      caches.match(event.request).then(cached => {
        if (cached) return cached;
        return fetch(event.request).then(response => {
          if (response && response.status === 200) {
            const clone = response.clone();
            caches.open(CACHE_NAME).then(cache => cache.put(event.request, clone));
          }
          return response;
        }).catch(() => cached);
      })
    );
  }
});
